import { createHash } from "node:crypto";
import { readdir, readFile, writeFile } from "node:fs/promises";
import { extname, join, relative, resolve, sep } from "node:path";

const defaultDistDir = resolve("dist");
const distDir = resolve(process.env.DIST_DIR ?? defaultDistDir);
const basePath = normalizeBasePath(process.env.BASE_PATH ?? "/");
const cachePrefix = "downtown-mayhem-precache";
const files = (await listFiles(distDir)).filter(shouldPrecache);
const manifestEntries = await Promise.all(
  files.map(async (file) => {
    const source = await readFile(file);
    const path = relative(distDir, file).split(sep).join("/");
    return {
      url: `${basePath}${path}`,
      revision: createHash("sha256").update(source).digest("base64url").slice(0, 12)
    };
  })
);
manifestEntries.unshift({ url: basePath, revision: revisionForIndex(manifestEntries) });

const cacheName = `${cachePrefix}-${createHash("sha256").update(JSON.stringify(manifestEntries)).digest("base64url").slice(0, 10)}`;
const source = `/* Generated by scripts/write-service-worker.mjs. */
const CACHE_PREFIX = ${JSON.stringify(cachePrefix)};
const CACHE_NAME = ${JSON.stringify(cacheName)};
const BASE_PATH = ${JSON.stringify(basePath)};
const PRECACHE_URLS = ${JSON.stringify(manifestEntries.map((entry) => entry.url), null, 2)};

self.addEventListener("install", (event) => {
  event.waitUntil(
    caches
      .open(CACHE_NAME)
      .then((cache) => cache.addAll(PRECACHE_URLS))
      .then(() => self.skipWaiting())
  );
});

self.addEventListener("activate", (event) => {
  event.waitUntil(
    caches
      .keys()
      .then((keys) => Promise.all(keys.filter((key) => key.startsWith(CACHE_PREFIX) && key !== CACHE_NAME).map((key) => caches.delete(key))))
      .then(() => self.clients.claim())
  );
});

self.addEventListener("fetch", (event) => {
  const request = event.request;
  if (request.method !== "GET") {
    return;
  }
  const url = new URL(request.url);
  if (url.origin !== self.location.origin || !url.pathname.startsWith(BASE_PATH)) {
    return;
  }
  if (request.mode === "navigate") {
    event.respondWith(networkFirstNavigation(request));
    return;
  }
  event.respondWith(cacheFirst(request));
});

async function networkFirstNavigation(request) {
  try {
    const response = await fetch(request);
    if (response.ok) {
      const cache = await caches.open(CACHE_NAME);
      await cache.put(request, response.clone());
    }
    return response;
  } catch {
    return (await caches.match(BASE_PATH)) || (await caches.match(BASE_PATH + "index.html")) || Response.error();
  }
}

async function cacheFirst(request) {
  const cached = await caches.match(request);
  if (cached) {
    return cached;
  }
  const response = await fetch(request);
  if (response.ok) {
    const cache = await caches.open(CACHE_NAME);
    await cache.put(request, response.clone());
  }
  return response;
}
`;

await writeFile(join(distDir, "sw.js"), source, "utf8");
console.log(`wrote service worker ${join(distDir, "sw.js")} with ${manifestEntries.length} precache entries`);

function shouldPrecache(file) {
  const path = relative(distDir, file).split(sep).join("/");
  if (path === "sw.js") {
    return false;
  }
  return [".html", ".js", ".css", ".json", ".wasm", ".webp", ".ogg"].includes(extname(file));
}

function normalizeBasePath(value) {
  const withLeadingSlash = value.startsWith("/") ? value : `/${value}`;
  return withLeadingSlash.endsWith("/") ? withLeadingSlash : `${withLeadingSlash}/`;
}

function revisionForIndex(entries) {
  const index = entries.find((entry) => entry.url.endsWith("/index.html"));
  return index?.revision ?? "root";
}

async function listFiles(directory) {
  const entries = await readdir(directory, { withFileTypes: true });
  const files = await Promise.all(
    entries.map((entry) => {
      const path = join(directory, entry.name);
      return entry.isDirectory() ? listFiles(path) : path;
    })
  );
  return files.flat();
}
