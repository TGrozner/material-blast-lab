function createPrebakedGroundBatches() {
  const panels = createGroundPanels();
  const groups = new Map();
  for (const panel of panels) {
    const key = `${String(panel.color)}:${panel.opacity}:${panel.layer}`;
    const group = groups.get(key);
    if (group) {
      group.push(panel);
    } else {
      groups.set(key, [panel]);
    }
  }

  return Array.from(groups.values()).map((group) => {
    const first = group[0];
    const geometry = mergedPanelGeometry(group);
    const batch = {
      name: group.length === 1 ? first.name : `${first.name} batch`,
      color: first.color,
      opacity: first.opacity,
      layer: first.layer,
      positions: roundArray(Array.from(geometry.getAttribute("position").array)),
      normals: roundArray(Array.from(geometry.getAttribute("normal").array)),
      uvs: roundArray(Array.from(geometry.getAttribute("uv").array)),
      indices: Array.from(geometry.index?.array ?? [])
    };
    geometry.dispose();
    return batch;
  });
}

